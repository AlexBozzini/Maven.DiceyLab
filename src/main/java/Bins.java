
public class Bins {
    private Integer lowestRoll;
    private Integer highestRoll;
    public Bins(){
        this.lowestRoll = 2;
        this.highestRoll = 12;
    }
    public Integer getLowestRoll(){
        return lowestRoll;
    }

    public Integer getHighestRoll() {
        return highestRoll;
    }
}
